def published_uk_capacity_tariffs() -> list[CapacityTariff]:
    """Return audited public UK National Gas NTS tariff rows."""

    tariffs: list[CapacityTariff] = []
    for point_name, point_type, values, page in ENTRY_TARIFF_ROWS:
        for gas_year, status, value in zip(GAS_YEARS, STATUSES, values, strict=True):
            tariffs.append(
                _tariff(
                    gas_year=gas_year,
                    point_name=f"{point_name} {point_type}",
                    point_id=_point_id(point_name, point_type),
                    direction="ENTRY",
                    value=value,
                    status=status,
                    source_table="Table 4 Entry Capacity Reserve Prices",
                    page=page,
                )
            )
    for point_name, point_type, values, page in EXIT_TARIFF_ROWS:
        for gas_year, status, value in zip(GAS_YEARS, STATUSES, values, strict=True):
            tariffs.append(
                _tariff(
                    gas_year=gas_year,
                    point_name=f"{point_name} {point_type}",
                    point_id=_point_id(point_name, point_type),
                    direction="EXIT",
                    value=value,
                    status=status,
                    source_table="Table 8 NTS TO Exit (Flat) Capacity Charges",
                    page=page,
                )
            )
    return tariffs


def _tariff(
    *,
    gas_year: str,
    point_name: str,
    point_id: str,
    direction: str,
    value: float,
    status: str,
    source_table: str,
    page: int,
) -> CapacityTariff:
    return CapacityTariff(
        tariff_id=(
            f"{point_id}-{direction.lower()}-{gas_year.replace('/', '-')}-"
            "firm-daily"
        ),
        document_id=DOCUMENT_ID,
        country="UK",
        tso="National Gas NTS",
        market_area="NTS",
        gas_year=gas_year,
        point_id=point_id,
        source_point_name=point_name,
        direction=TariffDirection(direction),
        capacity_product=CapacityProduct.DAILY,
        firmness=Firmness.FIRM,
        tariff_value=value,
        currency="GBP",
        unit="p/kWh/day",
        effective_from=date(int("20" + gas_year[:2]), 10, 1),
        effective_to=None,
        tariff_status=TariffStatus(status),
        source_table=source_table,
        source_page=page,
        source_refs=[
            DOCUMENT_REF,
            "National Gas effective from 1 October 2025",
            source_table,
            f"National Gas source URL {NATIONAL_GAS_TARIFF_URL}",
        ],
        manual_review_required=status != "FINAL",
    )


def _point_id(point_name: str, point_type: str) -> str:
    raw = f"{point_name} {point_type}".lower()
    slug = re.sub(r"[^a-z0-9]+", "-", raw).strip("-")
    return f"uk-ng-nts-{slug}"
